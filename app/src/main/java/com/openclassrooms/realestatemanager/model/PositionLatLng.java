package com.openclassrooms.realestatemanager.model;

public class PositionLatLng {
    String lat;
    String lng;

    public PositionLatLng() {}

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }
}
